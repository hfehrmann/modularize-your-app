import Foundation
import UIKit

public extension UIColor {

    static var lightningBlue: UIColor { return UIColor(displayP3Red: 0.22, green: 0.57, blue: 0.97, alpha: 1) }

    static var gray95: UIColor { return UIColor(hue: 0, saturation: 0, brightness: 0.95, alpha: 1) }
}
